export type LinkedLetter = {
  letter: Letter;
  next: LinkedLetter | null;
};

export function toLinkedLetter(val: string): LinkedLetter {
  if (val.length === 0) {
    return null;
  }
  return {
    letter: val[0] as Letter,
    next: toLinkedLetter(val.slice(1)),
  };
}

